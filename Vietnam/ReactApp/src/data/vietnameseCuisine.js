// Helper function to get cuisine data for a specific city
export const getCuisineDataForCity = (cityName) => {
  // Check if cityName is null or undefined
  if (!cityName || typeof cityName !== 'string') {
    return null;
  }

  // Normalize city name to match our data keys
  const normalizedCity = cityName.toLowerCase();

  if (normalizedCity.includes('hanoi')) {
    return vietnameseCuisineData['Hanoi'];
  } else if (normalizedCity.includes('hoi an')) {
    return vietnameseCuisineData['Hoi An'];
  } else if (normalizedCity.includes('da nang')) {
    return vietnameseCuisineData['Da Nang'];
  }

  return null;
};

// Helper function to get all specialties for a city
export const getSpecialtiesForCity = (cityName) => {
  const cityData = getCuisineDataForCity(cityName);
  return cityData ? cityData.specialties : [];
};

// Helper function to get restaurants for a city
export const getRestaurantsForCity = (cityName) => {
  const cityData = getCuisineDataForCity(cityName);
  return cityData ? cityData.restaurants : [];
};
